      <p>Trans African Clean Energy and Agriculture Services (TACEAS) is a  company limited by shares incorporated under Ugandan laws with the registrar of  companies. It is mandated to carry out services in Agriculture, Renewable  Energy, Development of Agriculture ICT solutions for Marketing through their  mobile Sales pipeline structure for various companies’ offering services to  rural farmers and renewable energy technologies that involve the construction  of domestic and institutional biogas plants for rural farmers.   <br><br>
        <font size="+2">Summary of Services</font></p>
      <ol>
        <li>Development  of Agriculture ICT solutions in Marketing for rural farmers.</li>
        <li>Licensing  of the Mobile sales pipeline dispatch system to various companies offering  services to rural farmers.</li>
        <li>Renewable  Energy technologies and services for rural farmers </li>
        <li>Research  and Development of generic data centers for consultancy and sales/marketing  purposes.</li>
      </ol>
      <p>Due to its highly qualified and competent staff members, partners and  associates, the company has grown to a cutting edge solutions provider in the  agriculture, Renewable energy technologies, and ICT sectors.<br>
        We strive towards provision of professional services to the ever growing  market for agriculture ICT solutions in Marketing using our Mobile sales  pipeline dispatch system, and Renewable energy technologies putting the  satisfaction of our clients as first priority.<br>
        TACEAS develops and distributes Agriculture ICT solutions and  technologies, Mobile sales pipeline dispatch system and offers renewable energy  technologies and services to organizations with national and International  branches in the delivery of quality life to rural areas in Africa. Key partners  in this regard include Village Power, Outbox Uganda Ltd, AgEco Tech, mobile  telecommunications companies, SNV, Bio Gas solutions Uganda Limited,  independent farmer SACCOs, savings groups, and Uganda’s Ministry of ICT.<br>
        TACEAS is a team of 5 members comprised of the CEO Mr. Kalinzi Ibrahim  Kiriisa, CIO Mr. Kiyini John Paddy, the Administrator Mrs. Sheila Leta, the Lead Business Developer Ms. Sauya Nakiyaga and Lead Sales  and Marketing Ms. Babirye Rebecca all Co-founders in charge of the day to day  affairs of the startup company. The team provides strategic direction to the  company and is responsible for mobilization of resources.</p>
